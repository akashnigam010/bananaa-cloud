CREATE SCHEMA IF NOT EXISTS `socyal` DEFAULT CHARACTER SET utf8 COLLATE utf8_general_ci;

CREATE TABLE `SOCYAL`.`ADDRESS` (
  `ID` INT NOT NULL AUTO_INCREMENT,
  `ADDRESS_LINE_1` VARCHAR(100) NULL,
  `ADDRESS_LINE_2` VARCHAR(100) NULL,
  `CITY` VARCHAR(100) NULL,
  `STATE` VARCHAR(100) NULL,
  `COUNTRY` VARCHAR(100) NULL,
  `ZIP` VARCHAR(20) NULL,
  `LATITUDE` DOUBLE NULL,
  `LONGITUDE` DOUBLE NULL,
  PRIMARY KEY (`ID`)) 
COLLATE='utf8_general_ci'
ENGINE=InnoDB
ROW_FORMAT=DEFAULT
AUTO_INCREMENT=1;

  
CREATE TABLE `SOCYAL`.`CONTACT` (
  `ID` INT NOT NULL AUTO_INCREMENT,
  `TELEPHONE` VARCHAR(20) NULL,
  `MOBILE` VARCHAR(20) NULL,
  `EMAIL` VARCHAR(100) NULL,
  PRIMARY KEY (`ID`)) 
COLLATE='utf8_general_ci'
ENGINE=InnoDB
ROW_FORMAT=DEFAULT
AUTO_INCREMENT=1;

CREATE TABLE `SOCYAL`.`TIMING` (
  `ID` INT NOT NULL AUTO_INCREMENT,
  `OPEN_TIME` DOUBLE NULL,
  `CLOSE_TIME` DOUBLE NULL,
  PRIMARY KEY (`ID`)) 
COLLATE='utf8_general_ci'
ENGINE=InnoDB
ROW_FORMAT=DEFAULT
AUTO_INCREMENT=1;
  
  
CREATE TABLE `SOCYAL`.`MERCHANT` (
  `ID` INT NOT NULL AUTO_INCREMENT,
  `NAME` VARCHAR(50) NULL,
  `ADDRESS_ID` INT NULL,
  `CONTACT_ID` INT NULL,
  `IMAGE_URL` VARCHAR(255) NULL,
  `RATING` DOUBLE NULL,
  `TIMING_ID` INT NULL,
  PRIMARY KEY (`ID`),
  INDEX `MERCHANT_ADDRESS_FK_idx` (`ADDRESS_ID` ASC),
  INDEX `MERCHANT_CONTACT_FK_idx` (`CONTACT_ID` ASC),
  INDEX `MERCHANT_TIMING_FK_idx` (`TIMING_ID` ASC),
  CONSTRAINT `MERCHANT_ADDRESS_FK`
    FOREIGN KEY (`ADDRESS_ID`)
    REFERENCES `SOCYAL`.`ADDRESS` (`ID`)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
  CONSTRAINT `MERCHANT_CONTACT_FK`
    FOREIGN KEY (`CONTACT_ID`)
    REFERENCES `SOCYAL`.`CONTACT` (`ID`)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
  CONSTRAINT `MERCHANT_TIMING_FK`
    FOREIGN KEY (`TIMING_ID`)
    REFERENCES `SOCYAL`.`TIMING` (`ID`)
    ON DELETE CASCADE
    ON UPDATE CASCADE) 
COLLATE='utf8_general_ci'
ENGINE=InnoDB
ROW_FORMAT=DEFAULT
AUTO_INCREMENT=1;

	
	